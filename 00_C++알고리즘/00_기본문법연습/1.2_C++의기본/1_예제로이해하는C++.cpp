#include <bits/stdc++.h>
// 헤더파일 STL 라이브러리를 import
// bits/stdc++ : 모든 표준 라이브러리가 포함된 헤더 
using namespace std;
// std라는 namespace사용
// cin cout등을 사용할때 std::cin이런식으로 사용해야함
// std를 기본으로 설정한다는 뜻 
string a;
// <타입> <변수명>  
int main(){
	cin >> a; // 입력 : cin, scanf 
	cout << a << "\n"; // 출력 : cout, printf 
	return 0; 
	// 프로세스가 일이 정상적으로 마무리되며 process exit call 한다는 뜻 
}
